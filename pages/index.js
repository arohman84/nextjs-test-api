const defaultState = () => {
  return {
    type: "object",
    properties: {
      prop: { type: "array" },
    },
  };
};

export default function Home() {
  const [inputAddress, setInputAddress] = useState();
  const [params, setParams] = useState();
  const [doc, setDoc] = useState(defaultState);
  const [config, setConfig] = useState(defaultState);
  const [response, setResponse] = useState();
  const [type, setType] = useState("get");

  const handleDocChange = useCallback((newDoc) => {
    // const val = JSON.parse(
    //   JSON.stringify(newDoc).replace(`\n`, "").replace(`\"`, '"')
    // );
    const val = JSON.parse(JSON.stringify(newDoc));
    console.log(val);
    console.log(val.type);
    setConfig(val);
  }, []);

  const handleParamsChange = useCallback((d) => {
    setParams(d);
  }, []);

  const handleSelectChange = (e) => {
    setType(e.target.value);
    // reset response
    setResponse();
  };

  const onHandleInputAddress = (e) => {
    setInputAddress(e.target.value);
  };

  const submitAPI = async () => {
    try {
      const Ajv = require("ajv");
      const ajv = new Ajv({ allErrors: true });

      let val = null;
      if (type === "get") {
        val = axios.get(inputAddress);
      } else if (type === "post") {
        val = axios.post(inputAddress, params);
      } else if (type === "put") {
        val = axios.put(inputAddress, params);
      } else {
        val = axios.delete(inputAddress);
      }
      await val
        .then((response) => {
          setResponse(response.data);
          // validate data
          const validate = ajv.compile(JSON.parse(config));
          const valid = validate(response.data);
          if (valid) {
            toast.success(<p>data is valid</p>);
          } else {
            console.log(validate.errors);
            validate.errors.map((i) =>
              toast.error(
                <p>
                  props: {i.instancePath}, {i.message}
                </p>
              )
            );
          }
        })
        .catch((e) => {
          toast.error(<p>{e.message}</p>);
        });
    } catch (error) {
      toast.error(<p>{error}</p>);
    }
  };

  return (
    <>
      <Head>
        <title>Test API</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="justify-center p-[6rem] pt-[1rem]">
        <div className="flex w-full gap-4 mt-10">
          <select
            className="max-w-xs select select-bordered"
            onChange={handleSelectChange}
          >
            <option value="get">GET</option>
            <option value="post">POST</option>
            <option value="put">PUT</option>
            <option value="delete">DELETE</option>
          </select>
          <div className="w-full form-control">
            <div className="input-group">
              <input
                type="text"
                placeholder="Searchâ€¦"
                className="w-full input input-bordered"
                value={inputAddress}
                onChange={onHandleInputAddress}
              />
              <button className="btn btn-square w-[80px]" onClick={submitAPI}>
                <div>submit</div>
              </button>
            </div>
          </div>
        </div>
        {(type === "put" || type === "post") && (
          <div className="w-full mt-10">
            <BodyParams data={params} onChange={handleParamsChange} />
          </div>
        )}
        <div className="flex w-full gap-4 mt-10">
          <Config initialDoc={doc} onChange={handleDocChange} />
          <Response data={response} />
        </div>
      </main>
      <Toaster
        position="bottom-right"
        gutter={8}
        toastOptions={{
          duration: 10000,
        }}
      />
    </>
  );
}
